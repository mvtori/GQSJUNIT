package calculadora;

public class Calculadora {

	public int  SomaInteiros(int a, int b) {
		return a+b;
	}
	
	public int SubtraiInteiros(int a, int b) {
		return a-b;
	}
	
	public int MultiplicaInteiros(int a, int b) {
		return a*b;
	}
	
	public int DivideInteiros(int a, int b) {
		return a/b;
	}

}
